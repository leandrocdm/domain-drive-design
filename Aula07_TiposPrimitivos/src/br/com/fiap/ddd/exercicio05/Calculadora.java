package br.com.fiap.ddd.exercicio05;

public class Calculadora {

	/*a)Crie um metodo chamado somar, com 2 parametros de entrada, e
	um retorno. Esses parametros deverao ser somados (o primeiro mais
	o segundo) no bloco de instrucoes(corpo do metodo) e o total da
	adicao devera ser retornado*/
	
	double somar(double valor1, double valor2){
		
		return (valor1 + valor2);
		
	}
	
	/*b) Crie um metodo chamado subtrair, com 2 parametros de entrada,
	e um retorno. Esses parametros deverao ser subtraidos (o primeiro
	menos o segundo) e o total da subtracao devera ser retornado*/

	double subtrair(double valor1, double valor2){
		
		return (valor1 - valor2);
		
	}

	
}
